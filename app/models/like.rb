class Like < ApplicationRecord
  # belongs_toで多対１
  belongs_to :user
  belongs_to :post
  # user_id_はpost_idとの組み合わせで一意となるように設定。
  validates :user_id, presence: true, uniqueness: {scope: :post_id}
  validates :post_id, presence: true
end
